WITH InfiniteRows (RowNumber) AS (
	SELECT 1 
	UNION ALL -- Recursive member definition
	SELECT a.RowNumber + 1
	FROM   InfiniteRows a 
	WHERE  a.RowNumber < 1000
), DateWithRow as (
	SELECT RowNumber,
		CAST(PARSEDATETIME('2020-12-31', 'yyyy-MM-dd') AS DATE) as DateDay
	FROM   InfiniteRows
), RowDates as (
	SELECT *, DATEADD('DAY', RowNumber, DateDay) DateInc
	FROM   DateWithRow
), TmpDates as (
	select rd.RowNumber, rd.DateInc, DAY_OF_MONTH(rd.DateInc) DateDay, 
		MONTH(rd.DateInc) DateMonth, YEAR(rd.DateInc) DateYear, 
		ISO_DAY_OF_WEEK(rd.DateInc) DateDayOfWeek
	from RowDates rd
), TmpFechaMax as (
	select COALESCE(max(id), 0) FechaIdMax from FECHA
)
insert into FECHA (ID, FECHA, DIA, MES, ANIO, DIA_NOMBRE, DIA_NOMBRE_CORTO, FERIADO, LABORAL, FIN_SEMANA) 
select td.RowNumber + tfm.FechaIdMax, td.DateInc, td.DateDay, td.DateMonth, td.DateYear,
	d.nombre, d.nombre_corto, 'NO' DateHoliday, 
	case when DateDayOfWeek < 6 then 'SI' else 'NO' end DateWork,
	case when DateDayOfWeek > 5 then 'SI' else 'NO' end DateWeekend
from TmpDates td inner join DIA d on td.DateDayOfWeek = d.Id, TmpFechaMax tfm
;

